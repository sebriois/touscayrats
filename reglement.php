<h1>Le règlement</h1>
<p>
	<center>
		En faisant le choix d'inscrire votre enfant à l'école 
		Saint Jean,<br /> 
		vous choisissez aussi d'en respecter les règles.
	</center>
</p>
<h2>Fréquentation - absences scolaires - retards.</h2>
<ul>
	<li>
		La fréquentation régulière de l’école élémentaire 
		est obligatoire, conformément aux textes législatifs 
		et réglementaires en vigueur.
	</li>
	<li>
		La régularité est source de bonne évolution et de 
		meilleure organisation au sein de la classe, même en 
		maternelle.
	</li>
	<li>
		En cas d’absence, l’élève remettra obligatoirement 
		un certificat médical ou un mot d’excuse à son 
		enseignant, sur papier libre. Toute absence non 
		justifiée pourra être signalée à l’Inspection Académique,
		 comme prévu par la loi.
	</li>
	<li>
		Le strict respect des dates de vacances scolaires 
		(fixées à la fois par l’Education Nationale et par la 
		Direction de l’Ecole) s’impose.
	</li>
	<li>
		En cas de maladie contagieuse, les délais d’éviction 
		en vigueur seront imposés.
	</li>
</ul>

<h2>Horaires</h2>
<ul>
	<li>9h00 – 12h00</li>
	<li>13h30 – 16h45</li>
</ul>

<ul>
	<li>
		SOYEZ PONCTUELS, car tout retard perturbe le bon 
		déroulement de l’organisation de l’école (début des 
		cours, appel pour la cantine…). En cas de retard 
		exceptionnel et justifié merci de vous présenter à la
		 porte de l’établissement. Après 9 heures plus aucun 
		élève ne sera accepté en cours.
	</li>

	<li>
		L’école ouvre ses portes à 8h00. Si vous arrivez à 
		l’école avant 8h00, merci d’attendre à la porte de 
		l’école. Un enfant ne doit jamais être laissé seul 
		devant l’école.
		L’accueil des élèves qui déjeunent à la maison se fait
		à partir de 13h20.
	</li>
	<li>
		En aucun cas un élève ne doit quitter l’école en 
		dehors des heures normales de sortie.
	</li>
</ul>

<h2>Tenue vestimentaire - hygiène</h2>
<ul>
	<li>
		L’enfant devra être propre, y compris à la sieste s’il
		 fréquente l’école l’après-midi ; les couches sont 
		interdites.
	</li>
	<li>
		Les élèves se présenteront à l’école dans une tenue 
		vestimentaire correcte et sans excentricité, adaptée à
		leur âge et aux activités scolaires.
	</li>
	<li>
		Pour les maternelles, Privilégiez les tenues 
		pratiques : pas de salopette, de bretelles ni de body.
	</li>
	<li>
		Si vous tenez aux vêtements de vos enfants, pensez à
		les marquer à leur nom. Avant chaque période de vacances
		les vêtements seront retirés des porte-manteaux et donnés
		à une association caritative.
	</li>
	<li>
		Pour éviter les accidents,  les colliers et boucles 
		d’oreilles « pendantes » ainsi que les boucles d’oreilles
		pour les garçons sont interdits. 
	</li>
	<li>
		Chaque famille est chargée de :
		<ul>
			<li>
				De veiller quotidiennement à la propreté corporelle
					et à la propreté vestimentaire de son enfant.
			</li>
			<li>
				D’être particulièrement vigilante quant aux soins à
				apporter à la chevelure des enfants pour lutter contre
				les poux.
			</li>
		</ul>
	</li>
</ul>

<h2>Santé</h2>
<ul>
	<li>
		Un enfant fiévreux est mieux à la maison qu’à l’école.
	</li>
	<li>
		Conformément à la législation scolaire, aucun médicament
		n’est administré à l’école. En cas de problème de santé,
		les parents le signaleront à la directrice qui transmettra
		le dossier au médecin scolaire qui jugera de la nécessité
		d’établir un Projet d’Accueil Individualisé (P.A.I.).
	</li>
	<li>
		En cas de maladie contagieuse, les délais d’éviction 
		en vigueur seront respectés.
	</li>
	<li>
		En cas d’accident ou d’indisposition, même légère, 
		l’enfant préviendra immédiatement un adulte. Afin d’éviter
		les « mauvaises surprises » pensez à informer l’enseignant
		et la direction de tout changement de numéro de téléphone
		(personnel ou travail).
	</li>
</ul>

<h2>Goûter</h2>
<ul>
	<li>
		Pour les maternelles, une collation sera proposée le matin
		(fruit, laitage) fournie par l’école.
	</li>
	<li>
		Pour les enfants qui restent à la garderie du soir, un
		goûter léger est autorisé (fruit, laitage). Les chips,
		gâteaux apéritifs et bonbons sont proscrits.
	</li>
	<li>
		Seule l’eau nature est acceptée.
	</li>
	<li>
		Chewing-gum et sucettes restent à la maison.
	</li>
</ul>

<h2>Vie scolaire</h2>
<ul>
	<li>
		Politesse et respect sont de règle. Chacun fera preuve
		de correction et de courtoisie aussi bien entre élèves
		qu’entre élèves et adultes : parents, enseignants, 
		personnel de surveillance, personnel d’entretien, personnel
		du restaurant scolaire. Les auteurs de grossièretés (de
		violences verbales) et de brutalités (violences physiques)
		seront sanctionnés.
	</li>
	<li>
		Les familles sont priées de rapporter les différents 
		documents à signer dans les meilleurs délais.
	</li>
	<li>
		Les élèves sont responsables du matériel mis à leur
		disposition et doivent respecter celui de leurs camarades.
	</li>
	<li>
		Les locaux, murs, sanitaires, mobilier sont à respecter
		: les robinets, les portes, la chasse d’eau, les
		cuvettes, le papier WC … ne sont pas des jeux.
	</li>
	<li>
		Respectons notre environnement : tous les élèves 
		participent à la bonne tenue de leur classe, des locaux
		en général et de la cour. Des poubelles sont à la 
		disposition de tous, dans la cour et dans les classes.
		Chaque jour, des élèves sont chargés du « Contrôle 
		Propreté » de la cour, selon un planning établi.
	</li>
</ul>

<h2>Sécurité</h2>
<ul>
	<li>
		Il est interdit d’apporter à l’école tout objet
		dangereux qui pourrait blesser les autres et/ou 
		détériorer le matériel ainsi que des objets de valeur.
		Jeux électroniques, consoles, téléphones portables,
		briquets,… resteront à la maison. Faute de quoi ils
		seront confisqués et rendus aux parents des élèves
		concernés.
	</li>
	<li>
		L’école décline toute responsabilité en cas de perte,
		de dégradation ou d’échange de jouets ou d’objets de valeur.
	</li>
	<li>
		Pendant les récréations les élèves ne doivent sous
		aucun prétexte revenir dans les classes.
	</li>
	<li>
		Seuls les doudous et les tétines sont autorisés
		pour la sieste.
	</li>
	<li>
		Aucun enfant, aucun parent n’est autorisé à pénétrer
		dans l’établissement après les cours. Si un cahier
		a été oublié c’est regrettable mais préparer ses
		affaires avant de quitter la classe fait partie des
		apprentissages. 
	</li>
	<li>
		La responsabilité de l’école cesse dès qu’un enfant
		est confié à ses parents.
	</li>
	<li>
		La cour du soir est réservée aux élèves et au personnel,
		les parents ne peuvent y entrer.
	</li>
	<li>
		La surveillance des enfants aux abords de l’école 
		incombe aux parents.
	</li>
</ul>

<h2>Assurance</h2>
<ul>
	<li>
		Tout enfant inscrit doit justifier d’une assurance
		responsabilité civile scolaire et extra scolaire ou
		peut adhérer à celle proposée par l’école.
	</li>
	<li>
		Lorsqu’un élève se blesse à l’intérieur de l’école,
		l’accident doit être signalé immédiatement à la 
		directrice qui prendra les mesures nécessaires.
	</li>
</ul>
