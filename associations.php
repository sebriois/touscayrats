<h1 id="apel">APEL</h1>
<a href="www.apel.fr" target="blank">apel.fr</a>
L'APEL est une association loi 1901 composée de parents
bénévoles qui donnent de leur temps et mettent leurs 
compétences et talents au service des enfants de l'école.
<ul>
	<li>Elle accueille les nouveaux parents.</li>
	<li>
		Elle organise différentes actions tout au long de
		l'année (photos de classe, marché de Noël, vente de fleurs,
		fête de l'école...) dans le but de récolter des fonds.
		Ces derniers sont entièrement reversés à l'école pour 
		offrir aux enfants des sorties, des spectacles, des 
		activités diverses et du matériel ludique et/ou éducatif.
		Chaque parent d'élève est invité à rejoindre cette équipe. 
		Les réunions, qui ont lieu le soir, sont ouvertes à tous.
	</li>
</ul>
