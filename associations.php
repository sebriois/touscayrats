<h1 id="ddec">DDEC</h1>
<a href="http://ddec81-82.org/index.html" target="blank">DDEC81</a>
<p>
	La Direction Diocésaine de l'enseignement catholique d'Albi Montauban
	fédère l'ensemble des établissements d'enseignement privés
	catholiques du Tarn et du Tarn et Garonne, de la maternelle
	à l'enseignement supérieur.
</p>
Elle a pour mission de:
<ul>
	<li>
		Promouvoir l'Enseignement Catholique en organisant
		sa prospective, la nomination des chefs d'établissements,
		la formation des enseignants.
	</li>
	<li>
		Garantir la cohérence des initiatives au sein du diocèse
		pour valoriser les liens avec l'Église diocésaine.
	</li>
</ul>
