<h1>Le poney</h1>
L'école Saint Jean est situé sur le site du lycée agricole privé
de Touscayrats qui dispose d'installations équestres de qualité.
Les élèves de l'école bénéficient de cours d'équitation 
toutes les semaines, dès la toute petite section.

<h2>Une histoire et une tradition équestre:</h2>
<ul>
	<li> 
		Depuis plus de 20 ans le lycée propose aux élèves,
		de la 4ème au BTSA, l’enseignement et la pratique de 
		l’équitation. 
	</li>
	<li>
		L'équitation est utilisé, pour les enfants en difficultés,
		comme un outil de remédiation.
	</li>
</ul>

<h2>Les moyens matériels</h2>
<ul>
	<li>
		Le lycée est situé sur un domaine de 55 ha, au pied de
		la Montagne Noire, les parcelles sont toutes implantées 
		en prairies, destinées uniquement à l’alimentation des
		chevaux. Ces parcelles ont l’énorme avantage d’être
		toutes regroupées.
	</li>
	<li>Les écuries comptent 40 boxes et 30 stalles.</li>
	<li>
		La cavalerie compte 100 chevaux :
		<ul>
			<li>25 chevaux de loisirs</li>
			<li>18 chevaux qui permettent la formation des galops 3 et 4</li>
			<li>
				15 chevaux de sports destinés aux élèves en 
				préparation des galops 6 et 7
			</li>
			<li>
				2 juments poulinières, une anglo-arabe et une selle
				français et leurs poulains.
			</li>
		</ul>
	</li>
</ul>
	
<h2>Les élèves et les poneys</h2>
<ul>
	<li>
		Les cours d'équitation sont assurés par des moniteurs
		agréés et encadrés par des élèves en formation BP JEPS
		(Brevet Professionnel de la Jeunesse, de l’Education
		Populaire et du Sport)
	</li>
	<li>
		Les poneys sont adaptés à la taille des enfants et
		les cours d'équitation sont différenciés selon l'âge
		et le niveau de chaque élève.
	</li>
</ul>
<p>
