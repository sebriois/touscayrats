<h1>La finalité de l’école St Jean : Accueillir, Eduquer, Croire</h1>
<p>Une mission au  service des élèves qui s’enracine dans l’Evangile.  
Cette mission repose sur   trois  orientations : accueillir, éduquer, croire.
</p>

<h2>Accueillir</h2>
<p>
	Tout enfant est accueilli quelque soit son origine sociale, 
	culturelle. L’école St Jean est un lieu qui ouvre à l’inter
	culturel, l’inter générationnel pour faire grandir chaque 
	enfant en humanité.
</p>

<h2>Eduquer</h2>
<p>
	Tout enfant est capable de progresser et d’évoluer.
	L’école St Jean, est un lieu qui accompagne chaque enfant 
	pour grandir et s’accomplir dans toutes ses dimensions : 
	spirituelle, affective, cognitive, sociale, corporelle.
</p>

<h2>Croire</h2>
<p>
	Chacun est accueilli dans sa personne et dans le respect de 
	sa conscience et de ses convictions religieuses.
	Tout enfant a droit à faire l’expérience d’une communauté 
	vivante qui fait une place à chacun et à Dieu pour une 
	annonce explicite de l’Evangile.
	L’école St Jean est un lieu qui promeut un sens Chrétien de 
	l’Homme et qui propose des temps de culture religieuse et de 
	célébration.
</p>

<h1>
	Ces orientations se déclineront dans quatre domaines 
	complémentaires
</h1>
<ul>
	<li>Dans une pédagogie au service des élèves.</li>
	<li>Une éducation qui prend en compte l’élève dans sa globalité.</li>
	<li>Des propositions de la Parole annoncée, vécue et célébrée.</li>
	<li>Une co-responsabilité éducative qui engage l’école et la famille.</li>
</ul>

<h2>Une pédagogie au service des élèves</h2>
<ul>
	<li>expérimentation, innovation, bon sens, prise en compte du réel,</li>
	<li>ouverture, éveil de la curiosité,</li>
	<li>
		anticiper sur les difficultés et les besoins des élèves et
		mettre en place des dispositifs adaptés,
	</li>
	<li>qui prend en compte le développement des compétences (socle commun) </li>
	<li>une évaluation positive qui distingue évaluation formative et certificative.</li>
</ul>

<h2>
	Une éducation qui prend en compte l’élève dans sa globalité, 
	en s’appuyant sur des conceptions vécues et partagées
</h2>
<ul>
	<li>écoute, bienveillance, confiance, </li>
	<li>valorisation des qualités de chacun,</li>
	<li>regard personnalisé, chaque Etre est unique au regard de Dieu,</li>
	<li>engagement et responsabilité pour soi et pour les autres, </li>
	<li>partage de ce que l’on est et des actions que l’on mène (inter culturel, inter générationnel…)</li>
</ul>

<h2>Des propositions de la Parole annoncée, vécue et célébrée</h2>
<ul>
	<li>
		Vivre les relations humaines à l’intérieur de la 
		communauté éducative avec pour référence les valeurs
		Evangéliques.
	</li>
	<li>Témoigner par la première annonce de l’Evangile.</li>
	<li>
		Proposer des temps de catéchèse, animés par des équipes 
		constituées de parents et d’enseignants.
	</li>
	<li>
		Mettre en place des célébrations qui suivent l’année liturgique.
	</li>
</ul>

<h2>Une co responsabilité éducative qui engage l’école et la famille</h2>
<ul>
	<li>
		Mettre en place des relations de confiance, de 
		bienveillance tout en étant dans la vérité.
	</li>
	<li>
		Faire vivre une communauté éducative où chacun participe 
		dans son rôle et avec ses compétences à la réalité vécue du 
		projet éducatif.
	</li>
	<li>Proposer des rencontres sur des thèmes éducatifs.</li>
</ul>
<h2>
	Une école au sein d’un lycée agricole, en écho à son projet 
	d’établissement dont les points forts sont les suivants
</h2>
<ul>
	<li>Approche globale de l’élève,</li>
	<li>
		La nature, les animaux, l’environnement, l’écologie, le 
		développement durable,
	</li>
	<li>L’intergénérationnel,</li>
	<li>La culture,</li>
	<li>L’ouverture à l’international et à l’humanitaire.</li>
</ul>
