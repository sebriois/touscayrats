<h2>Dossier d'inscription</h2>

Pour les enfants déjà scolarisés:
<ul>
	<li>une copie du dossier scolaire complet</li>
	<li>
		le certificat de radiation de l'école précédente,
		dûment daté (à fournir juste avant l'arrivée de
		l'enfant)
	</li>
</ul>

Pour les enfants qui font leur entrée en maternelle:
<ul>
	<li>
		un certificat médical d'aptitude à la vie en collectivité
	</li>
</ul>
Pour tous:
<ul>
	<li>
		Photocopie du carnet de santé (page des vaccinations
		obligatoires)
	</li>
	<li>
		Un chèque de 45 € qui sera déduit de la facturation
		du premier trimestre
	</li>
	<li>
		Fiche de renseignements dûment remplie et règlement
		signé (donnés dans le dossier d'inscription).
	</li>
	<li>Une enveloppe timbrée à votre nom et adresse</li>
	<li>2 photos d'identité</li>
	<li>
		Photocopie du livret de famille (acte de mariage des 
		parents et acte de naissance de l'enfant)
	</li>
	<li>
		Le certificat de baptême si votre enfant est baptisé
	</li>
	<li>
		En cas de divorce ou de séparation des parents : une 
		copie du jugement
	</li>
</ul>

<b>Aucun dossier incomplet ne sera retenu.</b>

