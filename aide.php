<h1>Dispositifs d'aide aux élèves</h1>
<h2> Aide aux élèves</h2>
<ul>
	<li>
		A l'école Saint Jean, les enfants en difficultés sont pris 
		en charge par François Ricardou. Ce dernier est un enseignant
		expérimenté qui a choisi de suivre une formation complémentaire
		pour se spécialiser dans l'aide pédagogique aux élèves: il
		est "maître E".
	</li>
	<li>
		François a trois missions:
		<ul>
			<li>
				Prévenir: identifier et accompagner les enfants qui
				pourraient avoir besoin d'aide, notamment en cycle 2
				avec l'apprentissage de la lecture.
			</li>
			<li>
				Remédier: proposer des outils méthodologiques, 
				donner du sens aux apprentissages.
			</li>
			<li>
				Accompagner: il est personne ressource pour l'équipe
				enseignante. A la demande des enseignantes, il évalue
				les élèves qui rencontrent des difficultés et peut
				recevoir ces derniers en petits groupes, dans sa classe,
				pour travailler selon des objectifs pédagogiques clairement
				énoncés. Les parents de l'enfant sont informés du
				projet et des progrès de leur enfant.
			</li>
		</ul>
	</li>
</ul>
<h2> Les ateliers d'aide personnalisée ("APE")</h2>
<ul>
	<li>
		Depuis la rentrée de septembre 2008, les enfants ont
		24 heures de classe chaque semaine. A cela s'ajoutent,
		pour les enseignantes, 2 heures au service des élèves 
		en difficulté de toute l'école. 
	</li>
	<li>
		Des ateliers d'aide personnalisée sont proposés à
		certains élèves, par périodes, et selon un objectif 
		pédagogique précisé aux parents.
	</li>
	<li>
		Les ateliers ont lieu les lundi, mardi et jeudi de 13h
		à 13h30.
	</li>
	<li>
		L'accord des parents est demandé et un rendez vous avec
		l'enseignante est proposé afin d'expliquer les objectifs
		des ateliers proposés.
	</li>
</ul>

<h2> Les réunions d'équipe éducative.</h2>
<ul>
	<li>
		Les réunions d'équipe éducative ont lieu à l'école.
	</li>
	<li>
		Elles ont pour objet de réunir les parents de l'enfant,
		les acteurs scolaires (l'enseignante de l'enfant, la
		directrice, l'AVS le cas échéant, parfois l'enseignant
		spécialisé) et les partenaires de soin (orthophoniste, 
		psychomotricien(ne), psychologue, etc.) afin de faire le
		point sur les progrès et les difficultés de l'enfant.
	</li>
	<li>
		L'objectif est de travailler en partenariat, afin d'aider
		au mieux l'enfant et sa famille, dans la limite de ce qui
		est possible dans le cadre ordinaire scolaire en termes de
		temps, de moyens, de compétences.
	</li>
</ul>
